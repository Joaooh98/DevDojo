package academy.devdojo.maratonajava.introducao;

public class Aula07OperadoresUnarios {
    public static void main(String[] args) {
        int numero = 0;
        numero++;
        System.out.println("numero++: "+numero);
        numero--;
        System.out.println("numero--: "+numero);
        ++numero;
        System.out.println("++numero: "+numero);
        --numero;
        System.out.println("--numero: "+numero);
    }
}
